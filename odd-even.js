// odd number
function oddNumber(number){

   if(number % 2 == 1){
       return true;
   }
  else{
    return false;
  }
}
console.log(oddNumber(24));
console.log(oddNumber(11));

// even number
function evenNumber(number){
    if(number % 2 == 0){
        return true;
    }
    else{
        return false;
    }   
}
console.log(evenNumber(21));
console.log(evenNumber(20));
